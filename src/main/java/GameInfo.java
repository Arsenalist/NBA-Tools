public class GameInfo {
	public String location, stadium, tv, time, homeLogo, awayLogo, homeName, awayName, line, overUnder;
	int homeId, awayId;

	public String getLocation() {
		return location;
	}
	public String getStadium() {
		return stadium;
	}
	public String getTv() {
		return tv;
	}
	public String getTime() {
		return time;
	}
	public String getHomeLogo() {
		return homeLogo;
	}
	public String getAwayLogo() {
		return awayLogo;
	}
	public String getHomeName() {
		return homeName;
	}
	public String getAwayName() {
		return awayName;
	}
	public int getAwayId() {
		return awayId;
	}
	public int getHomeId() {
		return homeId;
	}

    public String getLine() {
        return line;
    }

    public String getOverUnder() {
        return overUnder;
    }
}